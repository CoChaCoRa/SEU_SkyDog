package vo;

public class WishList {
	private String Username;
	private String SKU;
	
	public void setUsername(String username) {
		this.Username = username;
	}
	public void setSKU(String sku) {
		this.SKU = sku;
	}
	
	public String getUsername() {
		return Username;
	}
	public String getSKU() {
		return SKU;
	}
}
